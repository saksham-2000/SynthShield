# ==========================================
def save_checkpoint(model, tokenizer, epoch: int, config: Dict, label: str = "epoch") -> None:
    """Unwrap Opacus/PEFT model and save LoRA adapter weights + tokenizer."""
    import os

    # Unwrap Opacus GradSampleModule wrapper
    base_model = model._module if hasattr(model, "_module") else model

    # Local save path
    local_path = os.path.join(config["local_checkpoint_dir"], f"{label}_{epoch}")
    os.makedirs(local_path, exist_ok=True)
    base_model.save_pretrained(local_path)
    tokenizer.save_pretrained(local_path)
    print(f"   💾 Checkpoint saved locally → {local_path}")

    # Optional: back up to Google Drive
    if config.get("save_to_drive", False):
        try:
            drive_path = os.path.join(config["drive_save_dir"], f"{label}_{epoch}")
            shutil.copytree(local_path, drive_path, dirs_exist_ok=True)
            print(f"   ☁️  Backed up to Drive → {drive_path}")
        except Exception as e:
            print(f"   ⚠️  Drive backup failed: {e}. Checkpoint is safe locally.")


