# MAIN TRAINING FUNCTION
# ==========================================
def train_private_lora(config: Dict) -> None:
    print("=" * 60)
    print("🚀 INITIALIZING PRIVACY-PRESERVING SYNTHETIC DATA GENERATOR")
    print("=" * 60)

    set_seed(config["seed"])

    # ── Mount Drive early so we don't fail at save time ────────
    if config.get("save_to_drive", False):
        try:
            from google.colab import drive
            drive.mount("/content/drive")
            print("✅ Google Drive mounted.")
        except Exception as e:
            print(f"⚠️  Could not mount Drive: {e}. Will save locally only.")
            config["save_to_drive"] = False

    # 1. Device
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"✅ Device: {device}")
    if device.type == "cuda":
        print(f"   GPU: {torch.cuda.get_device_name(0)}")
    else:
        print("   ⚠️  No GPU detected. Training will be slow.")

    # 2. Data
    print(f"\n📂 Loading data: {config['csv_path']}")
    try:
        real_df = pd.read_csv(config["csv_path"])
    except FileNotFoundError:
        raise FileNotFoundError(f"❌ File not found: {config['csv_path']}")

    columns = list(real_df.columns)
    print(f"   Rows: {len(real_df)} | Columns: {len(columns)}")
    row_texts = [serialize_row(real_df.iloc[i], columns) for i in range(len(real_df))]

    # 3. Model + LoRA
    print(f"\n🤖 Loading model: {config['model_name']}...")
    tokenizer = AutoTokenizer.from_pretrained(config["model_name"])
    model = AutoModelForSeq2SeqLM.from_pretrained(config["model_name"])
    lora_config = LoraConfig(
        r=8, lora_alpha=16, lora_dropout=0.05, bias="none",
        task_type=TaskType.SEQ_2_SEQ_LM, target_modules=["q", "v"]
    )
    model = get_peft_model(model, lora_config)
    model.to(device)
    model.train()
    print("   LoRA adapters attached.")

    # 4. DataLoader
    ds = TabularTextDataset(
        row_texts, tokenizer, config["prompt"],
        config["max_input_len"], config["max_target_len"]
    )
    base_loader = DataLoader(
        ds, batch_size=config["logical_batch_size"],
        shuffle=True, drop_last=True,
        pin_memory=(device.type == "cuda")
    )

    optimizer = torch.optim.AdamW(
        (p for p in model.parameters() if p.requires_grad), lr=config["lr"]
    )

    # 5. Privacy Engine
    sample_rate = config["logical_batch_size"] / len(ds)
    noise_multiplier = get_noise_multiplier(
        target_epsilon=config["target_epsilon"], target_delta=config["delta"],
        sample_rate=sample_rate, epochs=config["epochs"], accountant="rdp"
    )
    print(f"\n🔐 Privacy: Target ε={config['target_epsilon']} | Noise multiplier={noise_multiplier:.4f}")

    privacy_engine = PrivacyEngine(accountant="rdp")
    model, optimizer, private_loader = privacy_engine.make_private(
        module=model, optimizer=optimizer, data_loader=base_loader,
        noise_multiplier=noise_multiplier, max_grad_norm=config["max_grad_norm"],
        poisson_sampling=True
    )

    # 6. Training Loop
    print("\n🏋️  STARTING TRAINING")
    start_time = time.time()

    for epoch in range(1, config["epochs"] + 1):
        epoch_start = time.time()
        losses = []

        with BatchMemoryManager(
            data_loader=private_loader,
            max_physical_batch_size=config["physical_batch_size"],
            optimizer=optimizer
        ) as safe_loader:

            pbar = tqdm(safe_loader, desc=f"Epoch {epoch}/{config['epochs']}", leave=True)
            for batch in pbar:
                batch = {k: v.to(device) for k, v in batch.items()}
                optimizer.zero_grad()
                loss = model(**batch).loss
                loss.backward()
                optimizer.step()
                loss_val = loss.detach().item()
                losses.append(loss_val)
                pbar.set_postfix({"loss": f"{loss_val:.4f}"})

        avg_loss = np.mean(losses)
        eps_now = privacy_engine.get_epsilon(delta=config["delta"])
        epoch_dur = time.time() - epoch_start
        print(f"   ✅ Epoch {epoch} | Loss={avg_loss:.4f} | ε={eps_now:.3f} | Time={epoch_dur:.1f}s")
        print_gpu_status()

        # ── CHECKPOINT SAVE ──────────────────────────────────────
        if epoch % config["save_every_n_epochs"] == 0:
            save_checkpoint(model, tokenizer, epoch, config, label="epoch")

    total_time = time.time() - start_time
    print(f"\n🎉 Training Complete in {total_time / 60:.2f} minutes.")

    # ── FINAL SAVE ───────────────────────────────────────────────
    print("\n💾 Saving final model...")
    save_checkpoint(model, tokenizer, epoch="final", config=config, label="final")

    # 7. Generation
    print("\n🔮 GENERATING SYNTHETIC DATA")
    base_model = model._module if hasattr(model, "_module") else model
    base_model.eval()

    n_out = config["n_generate"] if config["n_generate"] is not None else len(real_df)
    generated_texts = []
    prompt_batch = [config["prompt"]] * config["physical_batch_size"]
    gen_pbar = tqdm(total=n_out, desc="Generating Rows")

    for start in range(0, n_out, config["physical_batch_size"]):
        bs = min(config["physical_batch_size"], n_out - start)
        toks = tokenizer(
            prompt_batch[:bs], return_tensors="pt", padding=True,
            truncation=True, max_length=config["max_input_len"]
        ).to(device)
        with torch.no_grad():
            gen_ids = base_model.generate(
                **toks, do_sample=True, temperature=config["temperature"],
                top_p=config["top_p"], max_new_tokens=config["max_new_tokens"]
            )
        texts = tokenizer.batch_decode(gen_ids, skip_special_tokens=True)
        generated_texts.extend(texts)
        gen_pbar.update(len(texts))

    gen_pbar.close()

    # 8. Save Output CSV
    print(f"\n💾 Processing {len(generated_texts)} generated rows...")
    synth_rows = [parse_row_text(t, columns) for t in generated_texts]
    synth_df = pd.DataFrame(synth_rows, columns=columns)
    for col in real_df.columns:
        if col in synth_df.columns and pd.api.types.is_numeric_dtype(real_df[col]):
            synth_df[col] = pd.to_numeric(synth_df[col], errors="coerce")

    synth_df.to_csv(config["out_csv"], index=False)
    print(f"✅ Synthetic data saved → {config['out_csv']}")
    print(f"   Original: {len(real_df)} rows | Synthetic: {len(synth_df)} rows")


