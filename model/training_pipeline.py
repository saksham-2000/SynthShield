def print_gpu_status():
    if torch.cuda.is_available():
        vram_allocated = torch.cuda.memory_allocated() / 1024**3
        vram_reserved = torch.cuda.memory_reserved() / 1024**3
        print(f"   [GPU] {torch.cuda.get_device_name(0)} | Allocated: {vram_allocated:.2f} GB | Reserved: {vram_reserved:.2f} GB")
    else:
        print("   [GPU Status] WARNING: Running on CPU.")

def set_seed(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)

def stringify_value(v) -> str:
    if pd.isna(v): return "MISSING"
    if isinstance(v, (float, np.floating)):
        if not np.isfinite(v): return "MISSING"
        return f"{float(v):.6g}"
    return str(v).replace("\n", " ").strip()

def serialize_row(row: pd.Series, columns: List[str]) -> str:
    return " | ".join(f"{c}: {stringify_value(row[c])}" for c in columns)

def parse_row_text(text: str, columns: List[str]) -> Dict[str, str]:
    out = {c: np.nan for c in columns}
    chunks = [p.strip() for p in text.split("|")]
    for part in chunks:
        if ":" not in part: continue
        k, v = part.split(":", 1)
        k = k.strip()
        if k in out: out[k] = v.strip()
    return out


