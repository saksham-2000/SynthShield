class TabularTextDataset(Dataset):
    def __init__(self, row_texts, tokenizer, prompt, max_input_len, max_target_len):
        self.row_texts = row_texts
        self.tokenizer = tokenizer
        enc = tokenizer(prompt, truncation=True, max_length=max_input_len,
                        padding="max_length", return_tensors="pt")
        self.prompt_ids = enc["input_ids"].squeeze(0)
        self.prompt_mask = enc["attention_mask"].squeeze(0)
        self.max_target_len = max_target_len

    def __len__(self):
        return len(self.row_texts)

    def __getitem__(self, idx):
        tgt_tok = self.tokenizer(
            self.row_texts[idx], truncation=True,
            max_length=self.max_target_len, padding="max_length", return_tensors="pt"
        )
        labels = tgt_tok["input_ids"].squeeze(0)
        labels[labels == self.tokenizer.pad_token_id] = -100
        return {
            "input_ids": self.prompt_ids.clone(),
            "attention_mask": self.prompt_mask.clone(),
            "labels": labels,
        }


